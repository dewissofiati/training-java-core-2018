package operator;
public class OperatorMatematika{
    public static void main(String[] args){
        Integer a = 10, b = 20;
        System.out.println("a + b = " + (a+b));
        System.out.println("b - a = " + (b-a));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a++ = " + (a++));
        System.out.println("a % 3 = " + (a%3));
    }
}